#include "stm32f1xx_hal.h"
#include "armrobo.h"
/********************************************************************************************************************************************************/
uint16_t contador = 0;
/********************************************************************************************************************************************************/
/**
  * @brief  This function...
  * @param  None
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	BlinkLed();
}
/********************************************************************************************************************************************************/
/**
  * @brief  This function...
  * @param  None
  * @retval None
  */
void BlinkLed(void)
{

	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_11);
	contador++;

}
